/**
 * Get localized province name
 * @param thaiName Original Thai name from database
 * @param lang Current language code ('th', 'en', 'cn')
 * @returns Localized name or original Thai name if not found/th
 */
export function getProvinceName(thaiName: string, lang: string): string {
  if (!thaiName) return "";
  if (lang === "th") return thaiName;

  // Normalize: trim and remove "จ." prefix if present
  const cleanName = thaiName.replace(/^จ\./, "").trim();

  const mapped = PROVINCES[cleanName];
  if (mapped) {
    return lang === "cn" ? mapped.cn : mapped.en;
  }

  // Fallback: Return original if no mapping found
  return thaiName;
}
